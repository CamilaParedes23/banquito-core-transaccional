  ERROR_ULTIMO VARCHAR(1000) NULL,
  CONSTRAINT PK_OUTBOX_ACCOUNT PRIMARY KEY (ID),
  CONSTRAINT UK_OUTBOX_ACCOUNT_UUID UNIQUE (UUID_EVENTO),
  CONSTRAINT CK_OUTBOX_ACCOUNT_ESTADO CHECK (ESTADO IN ('PENDIENTE','PUBLICADO','ERROR','DESCARTADO'))
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Eventos confiables del account-service';

CREATE VIEW V_SALDO_CUENTA_RESUMEN AS
SELECT UUID_CUENTA, NUMERO_CUENTA, UUID_CLIENTE, IDENTIFICACION_TITULAR, NOMBRE_TITULAR_REFERENCIA,
       ESTADO, SALDO_CONTABLE, SALDO_DISPONIBLE, MONTO_RETENIDO,
       ES_CUENTA_MATRIZ_PAGOS, ES_CUENTA_FAVORITA_PAGOS, PROPOSITO_CUENTA
FROM CUENTA;

CREATE INDEX IDX_CUENTA_CLIENTE ON CUENTA(UUID_CLIENTE, ESTADO);
CREATE INDEX IDX_CUENTA_NUMERO_ESTADO ON CUENTA(NUMERO_CUENTA, ESTADO);
CREATE INDEX IDX_RESERVA_CORRELACION ON RESERVA_PAGO_MASIVO(UUID_CORRELACION);
CREATE INDEX IDX_INSTRUCCION_RESERVA ON INSTRUCCION_PAGO_MASIVO_CORE(RESERVA_PAGO_MASIVO_ID, ESTADO);
CREATE INDEX IDX_TRANSACCION_CUENTA_FECHA ON TRANSACCION_CUENTA(CUENTA_ID, FECHA_CONTABLE, TIMESTAMP_TRANSACCION);
CREATE INDEX IDX_TRANSACCION_CANAL ON TRANSACCION_CUENTA(CANAL_ORIGEN, FECHA_CONTABLE);
CREATE INDEX IDX_OUTBOX_ACCOUNT_ESTADO ON OUTBOX_EVENT(ESTADO, FECHA_CREACION);
CREATE INDEX IDX_IDEMPOTENCIA_EXPIRACION ON REGISTRO_IDEMPOTENCIA(ESTADO, FECHA_EXPIRACION);
