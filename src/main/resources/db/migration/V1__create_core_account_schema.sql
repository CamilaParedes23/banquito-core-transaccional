CREATE TABLE REGISTRO_IDEMPOTENCIA (
  ID BIGINT AUTO_INCREMENT NOT NULL,
  UUID_REGISTRO CHAR(36) NOT NULL,
  ACTOR_UUID VARCHAR(80) NOT NULL,
  TIPO_OPERACION VARCHAR(60) NOT NULL,
  CLAVE_IDEMPOTENCIA VARCHAR(120) NOT NULL,
