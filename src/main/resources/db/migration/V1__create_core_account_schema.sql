CREATE TABLE HISTORIAL_ESTADO_CUENTA (
  ID BIGINT AUTO_INCREMENT NOT NULL,
  CUENTA_ID BIGINT NOT NULL,
  ESTADO_ANTERIOR VARCHAR(15) NOT NULL,
  ESTADO_NUEVO VARCHAR(15) NOT NULL,
  MOTIVO_CAMBIO VARCHAR(300) NOT NULL,
  UUID_USUARIO_CORE CHAR(36) NULL,
  FECHA_CAMBIO DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT PK_HIST_ESTADO_CUENTA PRIMARY KEY (ID),
  CONSTRAINT FK_HIST_ESTADO_CUENTA FOREIGN KEY (CUENTA_ID) REFERENCES CUENTA(ID) ON UPDATE RESTRICT ON DELETE RESTRICT,
