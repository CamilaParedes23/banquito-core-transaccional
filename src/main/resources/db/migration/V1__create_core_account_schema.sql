-- ============================================================================
-- BANCO BANQUITO V2 - CORE ACCOUNT SERVICE
-- Modelo fisico MySQL 8.4 LTS
-- Base: banquito_core_account_db
-- Proposito: cuentas, saldos, estados, transacciones, reservas de pagos masivos,
-- instrucciones recibidas del Switch, reversos y outbox.
