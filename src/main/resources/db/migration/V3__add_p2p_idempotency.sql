CREATE TABLE IF NOT EXISTS REGISTRO_IDEMPOTENCIA (
  ID BIGINT AUTO_INCREMENT NOT NULL,
  UUID_REGISTRO CHAR(36) NOT NULL,
  ACTOR_UUID VARCHAR(80) NOT NULL,
  TIPO_OPERACION VARCHAR(60) NOT NULL,
  CLAVE_IDEMPOTENCIA VARCHAR(120) NOT NULL,
  HASH_SOLICITUD CHAR(64) NOT NULL,
  UUID_CORRELACION CHAR(36) NULL,
  ESTADO VARCHAR(20) NOT NULL DEFAULT 'EN_PROCESO',
  HTTP_STATUS INT NULL,
  RESPUESTA_JSON JSON NULL,
  ERROR_CODIGO VARCHAR(80) NULL,
  FECHA_CREACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  FECHA_ACTUALIZACION DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FECHA_EXPIRACION DATETIME NOT NULL,
  VERSION INT NOT NULL DEFAULT 0,
  CONSTRAINT PK_REGISTRO_IDEMPOTENCIA PRIMARY KEY (ID),
  CONSTRAINT UK_IDEMPOTENCIA_UUID UNIQUE (UUID_REGISTRO),
  CONSTRAINT UK_IDEMPOTENCIA_ACTOR_OPERACION_CLAVE UNIQUE (ACTOR_UUID, TIPO_OPERACION, CLAVE_IDEMPOTENCIA),
  CONSTRAINT CK_IDEMPOTENCIA_ESTADO CHECK (ESTADO IN ('EN_PROCESO','COMPLETADA','FALLIDA')),
  INDEX IDX_IDEMPOTENCIA_EXPIRACION (ESTADO, FECHA_EXPIRACION)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
